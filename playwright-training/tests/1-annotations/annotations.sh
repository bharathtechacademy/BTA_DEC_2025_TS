//annotations in playwright :

1.test.describe() : Describe is useful, especially when we want to group multiple test cases together at one place. 
2.test.only(); test.only is going to help you to run only particular test case. (it will be used in both describe and test block)
3.test.skip(); test.skip is going to help you to skip particular test case.(it will be used in both describe and test block)
4.test.fixme(); test.fixme is going to help you to mark particular test case as fixme. 
5.test.fail(); test.fail is going to help you to mark particular test case as fail. 
6.test.slow(); test.slow is going to help you to mark particular test case as slow. 