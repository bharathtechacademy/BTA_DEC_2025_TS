// this is a print statement in javascript, we can print and check the output in the console window
// this is a single line comment (control + /)

console.log("Hello, World!"); 


/* There are different types of logging methods available in JavaScript. 
Console.info is meant for printing informational messages. 
Console.warn is meant for printing warning messages to alert the user. 
Console.error is meant for printing the error message to indicate something went wrong.  
==> short cut to comment multiple lines: select the lines and press (shift + alt + A)
*/

console.info("This is an informational message."); // for informational messages
console.warn("This is a warning message."); // for warning messages 
console.error("This is an error message."); // for error messages